#!/usr/bin/env node
/**
 * Scan F6 photo-wall scene folder and rewrite manifest.json from scene *.webp files.
 * Optionally generates carousel thumbnails under thumbs/.
 *
 * Usage:
 *   node scripts/sync-photo-wall-scene-manifest.mjs
 *   node scripts/sync-photo-wall-scene-manifest.mjs --no-thumbs
 *
 * Drop new files into:
 *   assets/features/F6_photoWall/scenes/scene-3x4-*.webp
 *   assets/features/F6_photoWall/scenes/scene-4x3-*.webp
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const SCENES_DIR = path.join(root, "assets/features/F6_photoWall/scenes");
const THUMBS_DIR = path.join(SCENES_DIR, "thumbs");
const MANIFEST_PATH = path.join(SCENES_DIR, "manifest.json");
const THUMB_MAX_EDGE = 384;
const SCENE_FILE_PATTERN = /^scene[-_]?(3x4|4x3)[-_]?(\d+)\.webp$/i;

const generateThumbs = !process.argv.includes("--no-thumbs");

function naturalCompare(a, b){
  return String(a).localeCompare(String(b), "en", { numeric: true, sensitivity: "base" });
}

function titleLabel(baseName){
  const match = String(baseName).match(/^scene[-_]?(3x4|4x3)[-_]?(\d+)$/i);
  if (!match) {
    return String(baseName).replace(/[-_]+/g, " ").trim();
  }
  const aspect = match[1].toLowerCase();
  const num = Number(match[2]);
  return aspect === "4x3" ? `橫式展場 ${num}` : `直式展場 ${num}`;
}

function inferAspect(fileName){
  const match = String(fileName).match(/(3x4|4x3)/i);
  return match ? match[1].toLowerCase() : "3x4";
}

function listSceneFiles(){
  if (!fs.existsSync(SCENES_DIR)) {
    fs.mkdirSync(SCENES_DIR, { recursive: true });
    return [];
  }
  return fs.readdirSync(SCENES_DIR)
    .filter(name => SCENE_FILE_PATTERN.test(name))
    .filter(name => fs.statSync(path.join(SCENES_DIR, name)).isFile())
    .sort(naturalCompare);
}

async function loadSharp(){
  try {
    const mod = await import("sharp");
    return mod.default;
  } catch {
    return null;
  }
}

async function generateThumb(sharp, sourcePath, targetPath){
  await sharp(sourcePath)
    .rotate()
    .resize({
      width: THUMB_MAX_EDGE,
      height: THUMB_MAX_EDGE,
      fit: "inside",
      withoutEnlargement: true
    })
    .webp({ quality: 82 })
    .toFile(targetPath);
}

async function syncScenes(){
  const files = listSceneFiles();
  if (!fs.existsSync(THUMBS_DIR)) {
    fs.mkdirSync(THUMBS_DIR, { recursive: true });
  }

  const sharp = generateThumbs ? await loadSharp() : null;
  if (generateThumbs && !sharp) {
    console.warn("[F6] sharp not installed — skipping thumbnail generation. Run: npm install --no-save sharp");
  }

  const items = [];
  for (const file of files) {
    const baseName = file.replace(/\.webp$/i, "");
    const aspect = inferAspect(file);
    const thumbFile = `thumbs/${file}`;
    const sourcePath = path.join(SCENES_DIR, file);
    const thumbPath = path.join(THUMBS_DIR, file);

    if (sharp) {
      await generateThumb(sharp, sourcePath, thumbPath);
      console.log(`[F6] thumb: ${path.relative(root, thumbPath)}`);
    }

    items.push({
      id: baseName,
      label: titleLabel(baseName),
      file,
      thumb: thumbFile,
      aspect
    });
  }

  items.sort((a, b) => {
    if (a.aspect !== b.aspect) return a.aspect.localeCompare(b.aspect);
    return naturalCompare(a.id, b.id);
  });

  const payload = {
    generatedAt: new Date().toISOString(),
    note: "Auto-generated by scripts/sync-photo-wall-scene-manifest.mjs — drop scene-3x4-*.webp / scene-4x3-*.webp here, then run the sync workflow.",
    items
  };

  fs.writeFileSync(MANIFEST_PATH, `${JSON.stringify(payload, null, 2)}\n`, "utf8");
  console.log(`[F6] ${items.length} scene(s) → ${path.relative(root, MANIFEST_PATH)}`);

  if (sharp) {
    const thumbFiles = new Set(files);
    for (const name of fs.readdirSync(THUMBS_DIR)) {
      if (!thumbFiles.has(name)) {
        fs.unlinkSync(path.join(THUMBS_DIR, name));
        console.log(`[F6] removed orphan thumb: thumbs/${name}`);
      }
    }
  }
}

syncScenes().catch(error => {
  console.error("[F6] scene sync failed:", error);
  process.exit(1);
});
