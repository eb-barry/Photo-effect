#!/usr/bin/env node
/**
 * Scan F7 gallery texture folders and rewrite manifest.json from *.webp files.
 *
 * Usage:
 *   node scripts/sync-f7-gallery-textures.mjs
 *
 * Drop files into:
 *   assets/features/F7_virtualGallery/textures/walls/wall-01.webp
 *   assets/features/F7_virtualGallery/textures/floors/floor-01.webp
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const TARGETS = [
  {
    kind: "wall",
    dir: path.join(root, "assets/features/F7_virtualGallery/textures/walls"),
    pattern: /^wall[-_]?(\d+)\.webp$/i,
    label: n => `牆面 ${n}`
  },
  {
    kind: "floor",
    dir: path.join(root, "assets/features/F7_virtualGallery/textures/floors"),
    pattern: /^floor[-_]?(\d+)\.webp$/i,
    label: n => `地板 ${n}`
  }
];

function naturalCompare(a, b){
  return String(a).localeCompare(String(b), "en", { numeric: true, sensitivity: "base" });
}

function syncTarget(target){
  const files = fs.readdirSync(target.dir)
    .filter(name => target.pattern.test(name))
    .sort(naturalCompare);

  const items = files.map(file => {
    const match = file.match(target.pattern);
    const number = match ? String(Number(match[1])) : file;
    const id = `${target.kind}-${String(number).padStart(2, "0")}`;
    return {
      id,
      label: target.label(number),
      file
    };
  });

  const manifest = {
    generatedAt: new Date().toISOString(),
    note: "Auto-generated by scripts/sync-f7-gallery-textures.mjs",
    items
  };

  fs.writeFileSync(path.join(target.dir, "manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`);
  console.log(`[F7] ${target.kind}: ${items.length} textures → ${target.dir}/manifest.json`);
}

for (const target of TARGETS) {
  if (!fs.existsSync(target.dir)) fs.mkdirSync(target.dir, { recursive: true });
  syncTarget(target);
}
